/**
 * @param {number[]} bills
 * @return {boolean}
 */
var lemonadeChange = function (bills) {
	let five = 0; // 5元个数
	let ten = 0; // 10元个数
	let flag = true;
	for (let i = 0; i < bills.length; i++) {
		let bill = bills[i];
		// 顾客付的是5元
		if (bill === 5) {
			five += 1;
		} else if (bill === 10) {
			// 顾客付的是10元
			if (five >= 1) {
				ten += 1; // 10元个数+1
				five -= 1; // 找零5元，5元个数-1
			} else {
				// 如果此时没有5元，说明不能找零
				flag = false;
				break;
			}
		} else if (bill === 20) {
			// 顾客付的是20元
			// 有两种情况，消费一个5元一个10元，或者消费3个5元
			if (five >= 1 && ten >= 1) {
				five -= 1;
				ten -= 1;
			} else if (five >= 3) {
				five = five - 3;
			} else {
				flag = false;
				break;
			}
		}
	}
	return flag;
};

